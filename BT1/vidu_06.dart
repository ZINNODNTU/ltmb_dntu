void main() {
  //các phép tính cơ bản
  int a = 10;
  int b = 20;
  print('a + b = ${a + b}');
  print('a - b = ${a - b}');
  print('a * b = ${a * b}');
  print('a / b = ${a / b}');
  //phép chia lấy phần nguyên
  print('a ~/ b = ${a ~/ b}');

  //phép gián và tính toán kết hợp
  int x = 10;
  x += 5;
}
