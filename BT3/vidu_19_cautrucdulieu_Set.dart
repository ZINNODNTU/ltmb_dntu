void main() {
  // ĐỊNH NGHĨA SET
  // Set là một tập hợp các phần tử không có thứ tự và không chứa phần tử trùng lặp.
  // Set không chứa phần tử trùng lặp.
  // Set không có thứ tự.
  // Set không hỗ trợ truy cập bằng chỉ số.
  // Set không hỗ trợ thay đổi giá trị của phần tử.
  // Set không hỗ trợ thêm phần tử.
  // Set không hỗ trợ xóa phần tử.

  // KHAI BÁO SET
  Set<String> set1 = {'A', 'B', 'C'}; // Khởi tạo set với kiểu dữ liệu String
  Set<int> set2 = {1, 2, 3}; // Khởi tạo set với kiểu dữ liệu int
  Set<String> set3 = Set(); // Khởi tạo set rỗng
  var set4 = Set<int>.from([1, 2, 3]); // Khởi tạo set từ List
}

